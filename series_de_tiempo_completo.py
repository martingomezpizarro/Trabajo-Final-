def generar_ruido(n, usar_garch, sigma_const, omega, alpha_coeffs, beta_coeffs, garch_p_order, garch_q_order, seed=42):
    """Genera el vector de errores (innovaciones)."""
    np.random.seed(seed) # Seed for reproducibility
    z = np.random.normal(0, 1, n)

    if not usar_garch:
        return z * np.sqrt(sigma_const), np.ones(n) * sigma_const

    # Simulación GARCH(p,q)
    errores = np.zeros(n)
    varianzas = np.zeros(n)

    # Calcular varianza incondicional para inicialización (aproximada)
    sum_alpha = sum(alpha_coeffs) if alpha_coeffs else 0
    sum_beta = sum(beta_coeffs) if beta_coeffs else 0

    if (1 - sum_alpha - sum_beta) <= 0:
        initial_variance = omega / 0.1 # Arbitrary value to avoid division by zero
        warnings.warn("Los parámetros GARCH implican varianza no estacionaria o inestable, usando un valor inicial de varianza predeterminado.")
    else:
        initial_variance = omega / (1 - sum_alpha - sum_beta)

    # Determine the maximum lag needed
    max_lag_p = garch_p_order if garch_p_order > 0 else 0
    max_lag_q = garch_q_order if garch_q_order > 0 else 0
    max_lag = max(max_lag_p, max_lag_q)

    # Initialize first values with the unconditional variance
    for t in range(max_lag):
        varianzas[t] = initial_variance
        errores[t] = z[t] * np.sqrt(varianzas[t])

    # Recurrence GARCH(p,q)
    for t in range(max_lag, n):
        current_variance = omega
        # Sum of alpha terms (lagged squared errors)
        for i in range(garch_p_order):
            current_variance += alpha_coeffs[i] * (errores[t - (i+1)]**2)
        # Sum of beta terms (lagged variances)
        for j in range(garch_q_order):
            current_variance += beta_coeffs[j] * varianzas[t - (j+1)]

        # Ensure variance is not negative
        varianzas[t] = max(0.0001, current_variance) # Small value to avoid zero or negative
        errores[t] = z[t] * np.sqrt(varianzas[t])

    return errores, varianzas

def aplicar_filtro_arma(errores, ar, ma, sar, sma, s):
    """Aplica la estructura SARMA a los errores."""
    # Convert lists of coefficients to polynomials
    ar_poly = np.r_[1, -np.array(ar)]
    ma_poly = np.r_[1, np.array(ma)]

    def expandir_estacional(coefs, s_period):
        if not coefs: return np.array([1])
        poly = np.zeros(len(coefs) * s_period + 1)
        poly[0] = 1
        for i, c in enumerate(coefs):
            poly[(i+1)*s_period] = -c
        return poly

    def expandir_estacional_ma(coefs, s_period):
        if not coefs: return np.array([1])
        poly = np.zeros(len(coefs) * s_period + 1)
        poly[0] = 1
        for i, c in enumerate(coefs):
            poly[(i+1)*s_period] = c
        return poly

    sar_poly = expandir_estacional(sar, s)
    sma_poly = expandir_estacional_ma(sma, s)

    ar_final = np.convolve(ar_poly, sar_poly)
    ma_final = np.convolve(ma_poly, sma_poly)

    serie_filtrada = lfilter(ma_final, ar_final, errores)
    return serie_filtrada

def aplicar_integracion(serie, d, D, s):
    """Aplica diferencias inversas (suma acumulada)."""
    resultado = serie.copy()

    # Regular integration (accumulate d times)
    for _ in range(d):
        resultado = np.cumsum(resultado)

    # Seasonal integration (seasonal accumulation D times)
    if D > 0:
        temp = resultado.copy()
        for _ in range(D):
            integrada = np.zeros_like(temp)
            for t in range(len(temp)): # Handle cases where t < s_period correctly
                if t < s:
                    integrada[t] = temp[t]
                else:
                    integrada[t] = temp[t] + integrada[t-s]
            temp = integrada
        resultado = temp

    return resultado

def generar_tendencia_det(n, tipo, b0, b1, b2):
    t = np.arange(n)
    if tipo == 'lineal':
        return b0 + b1 * t
    elif tipo == 'cuadratica':
        return b0 + b1 * t + b2 * (t**2)
    elif tipo == 'exponencial':
        return b0 * np.exp(b1 * t/100) # Scaled so it doesn't explode too quickly
    else:
        return np.zeros(n) + b0

