    innovaciones, volatilidad = generar_ruido(N_OBS, USAR_GARCH, VARIANZA_CONSTANTE,
                                              GARCH_OMEGA, GARCH_ALPHA_COEFS, GARCH_BETA_COEFS,
                                              GARCH_P_ORDER, GARCH_Q_ORDER, seed=seed_value)

    # 2. Apply SARMA structure (Stationary Component)
    componente_estacionario = aplicar_filtro_arma(innovaciones, AR_COEFS, MA_COEFS,
                                                  SAR_COEFS, SMA_COEFS, S_PERIODO)

    # 3. Apply Integration (Stochastic Trend)
    componente_estocastico = aplicar_integracion(componente_estacionario, D_ORDEN,
                                                 D_ESTACIONAL, S_PERIODO)

    # 4. Add Deterministic Trend
    tendencia = generar_tendencia_det(N_OBS, TIPO_TENDENCIA, BETA_0, BETA_1, BETA_2)

    # 5. Final Series
    serie_final = componente_estocastico + tendencia

