def _ols_detrend_series(series, trend_type):
    """Detrends a series using OLS based on the specified trend type."""
    if trend_type == 'ninguna' or trend_type not in ['lineal', 'cuadratica', 'exponencial']:
        return series

    t = np.arange(len(series))
    if trend_type == 'lineal':
        X = np.column_stack([np.ones(len(t)), t])
    elif trend_type == 'cuadratica':
        X = np.column_stack([np.ones(len(t)), t, t**2])
    elif trend_type == 'exponencial':
        # For 'exponencial', we perform a log-linear regression if values are positive,
        # otherwise, we might approximate with a linear trend for simplicity in detrending.
        # Here, for detrending, we use a linear approximation for 'exponencial' to extract a trend component.
        X = np.column_stack([np.ones(len(t)), t])

    try:
        model = OLS(series, X)
        results = model.fit()
        return results.resid
    except Exception as e:
        print(f"Error during OLS detrending: {e}")
        return series # Return original series if detrending fails

def _apply_differencing(series, d_order, D_order, s_period):
    """Applies non-seasonal and seasonal differencing to a series."""
    transformed_series = series.copy()

    # Apply non-seasonal differencing
    for _ in range(d_order):
        transformed_series = np.diff(transformed_series)

    # Apply seasonal differencing
    if D_order > 0 and s_period > 0:
        for _ in range(D_order):
            if len(transformed_series) >= s_period:
                transformed_series = transformed_series[s_period:] - transformed_series[:-s_period]
            else:
                print(f"Warning: Series too short for seasonal differencing with period {s_period}.")
                break
    return transformed_series

